package week02;

import java.util.Scanner;

public class Solution1938{
    public static void main(String[]args){
        Scanner scan = new Scanner(System.in);
        int numA = scan.nextInt();
        int numB = scan.nextInt();
        System.out.println(numA+numB);
        System.out.println(numA-numB);
        System.out.println(numA*numB);
        System.out.println(numA/numB);
    }
    
}